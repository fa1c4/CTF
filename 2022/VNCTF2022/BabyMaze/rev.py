def dfs(maze, x, y, path, visited):
    # reach terminating point then return path
    if maze[x][y] == 7:
        return path
    
    # 4 directions w s a d
    directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]
    
    # traverse 4 directions
    for dx, dy in directions:
        nx, ny = x + dx, y + dy
        # check if at board or visited or value==1
        if 0 <= nx < len(maze) and 0 <= ny < len(maze[0]) and not visited[nx][ny] and maze[nx][ny] != 1:
            visited[nx][ny] = True  # mark visited
            path.append((nx, ny))  # record path
            
            # recursivly call dfs function
            result = dfs(maze, nx, ny, path, visited)
            if result:  # if found the path then return
                return result
            
            # back trace
            path.pop()
    
    return None  # no path found then return None

def find_path(maze):
    # create visited maps
    visited = [[False for _ in range(len(maze[0]))] for _ in range(len(maze))]
    
    # start point (1, 1)
    start_x, start_y = 1, 1
    visited[start_x][start_y] = True
    
    # start dfs
    return dfs(maze, start_x, start_y, [(start_x, start_y)], visited)

def path_format_converter(path):
    mapped_path = ''
    directions_mapping = {
        (-1, 0): 'w', 
        (1, 0): 's', 
        (0, -1): 'a', 
        (0, 1): 'd'
    }
    for idx in range(1, len(path)):
        prePos = path[idx-1]
        curPos = path[idx]
        dx = curPos[0] - prePos[0]
        dy = curPos[1] - prePos[1]
        mapped_path += directions_mapping[(dx, dy)]

    return mapped_path


